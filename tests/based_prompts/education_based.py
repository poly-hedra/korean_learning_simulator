# [1] CoT 순서 재편 + 제약 정리 ← 지금 바로 가능
#
# as-is: - 관계 유형이 외부 주입값인데 실행 순서 안에 명시 없음
#           → LLM이 관계 유형을 언제 참고하는지 불명확
#         - ## 제약과 ## 실행 순서 간 중복 내용 다수
#           → role 조건: 제약 ①에도, 실행 순서 3번에도
#           → mission 조건: 제약 ④에도, 실행 순서 5번에도
#
# to-be: - 실행 순서 0번에 관계 유형 명시
